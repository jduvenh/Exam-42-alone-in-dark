#include <unistd.h>

int		main(int ac, char **av)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (ac == 2)
	{
		while (( av[1][i] == ' ') || (av[1][i] == '\t'))
			i++;
		while (av[1][j])
			j++;
		while ((av[1][j - 1] == ' ') || ( av[1][j - 1] == '\t'))
			j--;
		while ( av[1][i] != '\0')
		{
			write(1, &av[1][i] , 1);
			i++;
			if ((av[1][i] == ' ') || (av[1][i] == '\t'))
			{
				if ( i < j)
					write(1,"   ", 3);
				while ((av[1][i] == ' ' ) || ( av[1][i] == '\t'))
					i++;
			}
		}
	}
	write(1, "\n", 1);
	return (0);
}
